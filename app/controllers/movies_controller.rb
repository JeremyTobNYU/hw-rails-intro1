class MoviesController < ApplicationController
  before_action :set_movie, only: %i[ show edit update destroy ]


  # GET /movies or /movies.json
  def index
    puts "params: #{params.to_unsafe_h.inspect}"

    @all_ratings = Movie.all_ratings

    # puts @sort

    if params[:ratings]
      @ratings_to_show = params[:ratings].keys
    else
      @ratings_to_show = Movie.all_ratings
    end

    if params[:sort_by]
      @sort = params[:sort_by].to_sym
    else
      @sort = :title #default sort should be title because it is the first element seen in drop down menu
    end

    @movies = Movie.with_rating(@ratings_to_show, @sort)
    # @all_ratings = @ratings_to_show

  end

  # GET /movies/1 or /movies/1.json
  def show
  end

  # GET /movies/new
  def new
    @movie = Movie.new
  end

  # GET /movies/1/edit
  def edit
  end

  # POST /movies or /movies.json
  def create
    @movie = Movie.new(movie_params)

    respond_to do |format|
      if @movie.save
        format.html { redirect_to @movie, notice: "Movie was successfully created." }
        format.json { render :show, status: :created, location: @movie }
      else
        format.html { render :new, status: :unprocessable_entity }
        format.json { render json: @movie.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /movies/1 or /movies/1.json
  def update
    respond_to do |format|
      if @movie.update(movie_params)
        format.html { redirect_to @movie, notice: "Movie was successfully updated." }
        format.json { render :show, status: :ok, location: @movie }
      else
        format.html { render :edit, status: :unprocessable_entity }
        format.json { render json: @movie.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /movies/1 or /movies/1.json
  def destroy
    @movie.destroy!

    respond_to do |format|
      format.html { redirect_to movies_path, status: :see_other, notice: "Movie was successfully destroyed." }
      format.json { head :no_content }
    end
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_movie
      @movie = Movie.find(params[:id])
    end

    # Only allow a list of trusted parameters through.
    def movie_params
      params.require(:movie).permit(:title, :rating, :description, :release_date)
    end
end
